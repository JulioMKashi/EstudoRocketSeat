let numberOne = prompt ("Digite um numero")
let numberTwo = prompt ("Digite o segundo numero")

const sum = prompt(numberOne + numberTwo)
const sub = prompt(numberOne - numberTwo)
const mult = prompt(numberOne * numberTwo)
const div = prompt(numberOne / numberTwo)
const rest = prompt(numberOne % numberTwo)

alert("A soma dos numeros deu: " + sum)
alert("A subtracao dos numeros deu: " + sub)
alert("A multiplicacao dos numeros deu: " + mult)
alert("A divisao dos numeros deu: " + div)
alert("O resto dos numeros deu: " + rest)
